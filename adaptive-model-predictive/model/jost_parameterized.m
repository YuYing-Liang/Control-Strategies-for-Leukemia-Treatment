function [dfdx,dfdu,dgdx] = jost_parameterized(p,x1_sym,x2_sym,x3_sym,x4_sym,x5_sym,x6_sym,x7_sym,x8_sym,u_sym,t_s)

    f = [x1_sym;x2_sym;x3_sym;x4_sym;x5_sym;x6_sym;x7_sym;x8_sym] + t_s * ...
        ([-p(1)                0 0 0 0 0 0 0; ...
        p(1) -p(2)          0 0 0 0 0 0; ...
        0 p(3)*p(4) -p(5) 0 0 0 0 0; ...
        0 0 0 -p(7)                 0 0 0 0; ...
        0 0 0 p(7) -p(7)          0 0 0; ...
        0 0 0 0 p(7) -p(7)          0 0; ...
        0 0 0 0 0 p(7) -p(7)          0; ...
        0 0 0 0 0 0 p(7) -p(10)]*[x1_sym;x2_sym;x3_sym;x4_sym;x5_sym;x6_sym;x7_sym;x8_sym] + ...
        [0.22;0;0;0;0;0;0;0]*u_sym + ...
        [0;...
        0;...
        0;...
        p(7)*((p(6)/x8_sym)^p(9))*x4_sym-p(7)*p(8)*((p(6)/x8_sym)^p(9))*x3_sym*x4_sym;...
        0;...
        0;...
        0;...
        0]);

    dfdx = jacobian(f,[x1_sym;x2_sym;x3_sym;x4_sym;x5_sym;x6_sym;x7_sym;x8_sym]);
    dfdu = jacobian(f,u_sym);

    g = (x8_sym);
    dgdx = jacobian(g,[x1_sym;x2_sym;x3_sym;x4_sym;x5_sym;x6_sym;x7_sym;x8_sym]);
    
end